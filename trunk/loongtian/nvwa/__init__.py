"""
术语表：

感知器：
    是一种特殊的观察者。可以直接将外部信息传递并转化为Nvwa大脑可以“理解”的对象，
    这里是指人类接收外界信息的器官，
    例如眼（视觉）、耳（听觉）、鼻（味觉）、口（味觉），手和皮肤（触觉）。
    在女娲系统中指接收外界输入的功能组件，
    可以接收有文本、图片、影音、多媒体等信息。
    目前只处理文本。
    当外部感知器传递的信息经过大脑的处理后，由“我”按规则再进行特殊的处理时，“我”也成为一种特殊的感知器。
RealObject：
	针对文本而言，指的是每个独立的词（可以是单个字或者多个字），
	是构成整个女娲大脑信息的最小粒度元素。
关系：
	指的是RealObject之间的关联。关联本身也是RealObject。
	整个系统是由关系连接RealObject组成的大网络。
    所有进入系统的信息都是关系，不会彻底删除。
    系统根据一定算法计算其查询时的排级顺序。
    也可以称为知识或记忆，就是女娲数据网络中的所有信息。
Metadata：
    指从RealObject中提炼出来的术语词。
    主要包括一词多义情况（例如：简爱代表小说、电影、人物等），
    或者是组合词术语（例如：火车、中国银行等）。
    当然也包括一词一义的情况。
Action：
	代表某种动作。可以由多个步骤组成，每个步骤会使主体状态产生变化。
    Action会有单独的表独立存储，记录其每个变化轨迹。
Modifier：
	代表修饰词。例如：好、大、多、长等。
    本身也是RealObject。
    （现在已合并到Action中。）
论域：
    讨论的话题圈子。一个Mind应该对应一个论域。
分组器：
	在某论域范围内，具有相同关系属性的一组RealObject形成的集合。
	本身也是一种RealObject。
    例如：牛有腿，牛有头，牛有尾。“腿、头、尾”可以构成一个分组器。
迁移：
    将MetaData转换为机器可理解的知识。
激活：
	对每个对象激活时，该对象直接关联的对象也应轻度激活，隔一层的对象次轻度激活，形成波纹激活圈。
激活度：
	依据一定算法，根据关系或者Metadata的访问频率、强度、最后访问时间等计算得出数值。
    是动态形成的，不在数据库中保存（可以在缓存总保存）。
    数值越高，激活度越高。
    为检索和匹配过程中排序排级使用。
置信度：
	依据一定算法，根据消息来源历史记录判断新消息可信程度。
    数值越高，可信度越高。
阈值：
	属性的访问门槛。激活度超过这个值才会被访问到。尤其适用于一词多义情况时候选出目标。
Mind：
	代表一组话题的容器，技术上对应于线程。容器中会留存许多进入系统的记忆输入信息。
分词引擎：
	第三方分词组件（选用jieba分词）。功能是将输入文本整句话拆分成“词”的列表。以供后续处理。
    其词库和词频在未来可以根据使用情况扩展和调整。
女娲大脑/Brain：
	指整个系统，或持续运行的总调度服务。
"""

